import React, { useCallback, useMemo, useState } from "react";
import { PageLayout, useFormValidation } from "@agent-management-platform/views";
import { generatePath, useNavigate, useParams } from "react-router-dom";
import { absoluteRouteMap, type CreateMonitorRequest } from "@agent-management-platform/types";
import { CreateMonitorForm } from "./subComponents/CreateMonitorForm";
import { SelectPresetMonitors } from "./subComponents/SelectPresetMonitors";
import { Alert, Button, Stack } from "@wso2/oxygen-ui";
import { ArrowLeft, ArrowRight } from "@wso2/oxygen-ui-icons-react";
import { useCreateMonitor } from "@agent-management-platform/api-client";
import { createMonitorSchema, type CreateMonitorFormValues } from "./form/schema";

export const CreateMonitorComponent: React.FC = () => {
    const { agentId, envId, orgId, projectId } = useParams<{
        agentId: string, envId: string, orgId: string, projectId: string
    }>();
    const navigate = useNavigate();
    const { mutate: createMonitor, isPending, error } = useCreateMonitor({
        orgName: orgId,
    });

    const defaultTimeRange = useMemo(() => {
        const end = new Date();
        const start = new Date(end.getTime() - 24 * 60 * 60 * 1000);
        return { start, end };
    }, []);

    const [page, setPage] = useState<1 | 2>(1);
    const [formData, setFormData] = useState<CreateMonitorFormValues>({
        displayName: "",
        name: "",
        description: "",
        type: "past",
        traceStart: defaultTimeRange.start,
        traceEnd: defaultTimeRange.end,
        intervalMinutes: 60,
        samplingRate: 25,
        evaluators: [],
    });

    const { errors, validateForm, setFieldError, validateField } =
        useFormValidation<CreateMonitorFormValues>(createMonitorSchema);

    const slugify = useCallback((value: string) =>
        value
            .toLowerCase()
            .replace(/[^a-z0-9]+/g, "-")
            .replace(/^-+|-+$/g, "")
            .slice(0, 60), []);

    const missingParamsMessage = useMemo(() => {
        if (!orgId) return "Organization is required to create a monitor.";
        if (!projectId) return "Project context is required.";
        if (!agentId) return "Select an agent before creating a monitor.";
        if (!envId) return "Select an environment before creating a monitor.";
        return null;
    }, [agentId, envId, orgId, projectId]);

    const handleFieldChange = useCallback(
        (field: keyof CreateMonitorFormValues, rawValue: unknown) => {
            setFormData((prev) => {
                let value = rawValue;
                if ((field === "intervalMinutes" || field === "samplingRate") && typeof rawValue === "string") {
                    value = rawValue ? Number(rawValue) : undefined;
                }
                const next = { ...prev, [field]: value } as CreateMonitorFormValues;

                if (field === "displayName") {
                    const autoGenerated = slugify(String(rawValue ?? ""));
                    const previousAuto = slugify(prev.displayName ?? "");
                    if (!prev.name || prev.name === previousAuto) {
                        next.name = autoGenerated;
                    }
                }

                const fieldError = validateField(field, next[field], next);
                setFieldError(field, fieldError);

                if (field === "displayName" || field === "name") {
                    const slugError = validateField("name", next.name, next);
                    setFieldError("name", slugError);
                }

                return next;
            });
        }, [setFieldError, slugify, validateField]);

    const handleToggleEvaluator = useCallback((evaluatorName: string) => {
        setFormData((prev) => {
            const exists = prev.evaluators.some((evaluator) => evaluator.name === evaluatorName);
            const nextEvaluators = exists
                ? prev.evaluators.filter((evaluator) => evaluator.name !== evaluatorName)
                : [...prev.evaluators, { name: evaluatorName }];

            const next = { ...prev, evaluators: nextEvaluators } as CreateMonitorFormValues;
            const evalError = validateField("evaluators", nextEvaluators, next);
            setFieldError("evaluators", evalError);
            return next;
        });
    }, [setFieldError, validateField]);

    const handleSaveEvaluatorConfig = useCallback((evaluatorName: string,
        config: Record<string, unknown>) => {
        setFormData((prev) => {
            const exists = prev.evaluators.some((evaluator) => evaluator.name === evaluatorName);
            const nextEvaluators = exists
                ? prev.evaluators.map((evaluator) =>
                    evaluator.name === evaluatorName ? { ...evaluator, config } : evaluator)
                : [...prev.evaluators, { name: evaluatorName, config }];
            const next = { ...prev, evaluators: nextEvaluators } as CreateMonitorFormValues;
            const evalError = validateField("evaluators", nextEvaluators, next);
            setFieldError("evaluators", evalError);
            return next;
        });
    }, [setFieldError, validateField]);

    const handleCreateMonitor = useCallback(() => {
        if (missingParamsMessage) {
            return;
        }

        if (!validateForm(formData) || !orgId || !projectId || !agentId || !envId) {
            setPage(1);
            return;
        }

        const payload: CreateMonitorRequest = {
            name: formData.name.trim(),
            displayName: formData.displayName.trim(),
            projectName: projectId,
            agentName: agentId,
            environmentName: envId,
            evaluators: formData.evaluators,
            type: formData.type,
            intervalMinutes: formData.intervalMinutes ?? undefined,
            traceStart: formData.traceStart ? formData.traceStart.toISOString() : undefined,
            traceEnd: formData.traceEnd ? formData.traceEnd.toISOString() : undefined,
            samplingRate: (formData.samplingRate ?? 0) / 100,
        };

        createMonitor(payload, {
            onSuccess: () => {
                navigate(
                    generatePath(
                        absoluteRouteMap.children.org.children.projects.children.agents
                            .children.environment.children.evaluation.children.monitor.path,
                        { orgId, envId, projectId, agentId }
                    )
                );
            },
        });
    }, [agentId, createMonitor, envId,
        formData, missingParamsMessage, navigate, orgId, projectId, validateForm]);

    const canAdvance = formData.displayName.trim().length >= 3 && formData.name.trim().length >= 3;
    const scheduleReady = formData.type === "future" || (!!formData.traceStart && !!formData.traceEnd);
    const evaluatorsSelected = formData.evaluators.length > 0;
    const isSubmissionReady = canAdvance &&
        scheduleReady && evaluatorsSelected && !missingParamsMessage;

    return (
        <PageLayout
            title="Create Monitor"
            description="Define a new eval monitor by selecting targets, thresholds, and alerting rules."
            disableIcon
            backLabel="Back to Monitors"
            backHref={
                generatePath(
                    absoluteRouteMap.children.org.children.projects.children.agents
                        .children.environment.children.evaluation.children.monitor.path,
                    { orgId, envId, projectId, agentId })
            }
        >
            <Stack spacing={3}>
                {missingParamsMessage && (
                    <Alert severity="error">
                        {missingParamsMessage}
                    </Alert>
                )}
                {!!error && (
                    <Alert severity="error">
                        {error instanceof Error ? error.message : "Failed to create monitor"}
                    </Alert>
                )}
                {
                    page === 1 &&
                    <CreateMonitorForm
                        formData={formData}
                        errors={errors}
                        onFieldChange={handleFieldChange}
                    />
                }

                {
                    page === 2 && (
                        <SelectPresetMonitors
                            selectedEvaluators={formData.evaluators}
                            onToggleEvaluator={handleToggleEvaluator}
                            onSaveEvaluatorConfig={handleSaveEvaluatorConfig}
                            error={errors.evaluators}
                        />
                    )
                }
                <Stack direction="row" gap={2} >
                    {
                        page === 1 && (
                            <>
                                <Button
                                    variant="contained"
                                    color="primary"
                                    endIcon={<ArrowRight size={20} />}
                                    onClick={() => setPage(2)}
                                    disabled={!canAdvance || Boolean(missingParamsMessage)}
                                >
                                    Next
                                </Button>
                            </>
                        )
                    }
                    {
                        page === 2 && (
                            <>
                                <Button variant="outlined" startIcon={<ArrowLeft size={20} />} color="primary" onClick={() => setPage(1)}>
                                    Previous
                                </Button>
                                <Button
                                    variant="contained"
                                    color="primary"
                                    disabled={!isSubmissionReady || isPending}
                                    onClick={handleCreateMonitor}
                                >
                                    Create Monitor
                                </Button>
                            </>
                        )
                    }
                </Stack>
            </Stack>
        </PageLayout>
    )
};

export default CreateMonitorComponent;
